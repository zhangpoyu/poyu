package come.poyu.entity;

public class UnruleCar {
    private int id;
    private String name;
    private String number;
    private String car_number;
    private String unrule_record;
    private String record_deal;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getCar_number() {
        return car_number;
    }

    public void setCar_number(String car_number) {
        this.car_number = car_number;
    }

    public String getUnrule_record() {
        return unrule_record;
    }

    public void setUnrule_record(String unrule_record) {
        this.unrule_record = unrule_record;
    }


    public String getRecord_deal() {
        return record_deal;
    }

    public void setRecord_deal(String record_deal) {
        this.record_deal = record_deal;
    }
}
